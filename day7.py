marks = int(input("Enter your marks: "))

if marks >= 90:
    print("Congratulations! You are an A-Grade! ")
elif marks >= 80:
    print("Nice! you've got a B-grade!")
elif marks >= 70:
    print("You've got C (I know you can do better!)")
elif marks >= 60:
    print("It's a D!. You better start STUDYING!")
else:
    print("You have Failed the test.")
